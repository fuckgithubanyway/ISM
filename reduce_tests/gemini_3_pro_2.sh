#!/bin/sh

# gemini_3_pro_v3.sh — POSIX-совместимый скрипт для редукции массивов.
# Исправлена обработка многострочных массивов и регулярные выражения.

awk '
# Проверка: является ли элемент "безопасным" литералом данных
function is_lit(s) {
    # Удаляем пробелы, табы и переносы строк по краям
    gsub(/^[ \t\n\r]+|[ \t\n\r]+$/, "", s)
    if (s == "") return 1
    
    # 1. Числа (целые, дробные, hex, bin, экспоненты, суффиксы f/L/u)
    if (s ~ /^[-+]?([0-9]+(\.[0-9]*)?|\.[0-9]+)([eE][-+]?[0-9]+)?[fFlLuU]*$/) return 1
    if (s ~ /^0[xXbB][0-9a-fA-F_]+$/) return 1
    
    # 2. Строки (в двойных или одинарных кавычках)
    if (s ~ /^"[^"]*"$/ || s ~ /^\047[^\047]*\047$/) return 1
    
    # 3. Константы
    if (s ~ /^(true|false|null|None|Nil|undefined|TRUE|FALSE)$/i) return 1
    
    # 4. Вложенные структуры (если внутри нет присваиваний или логики)
    if (s ~ /^[[({].*[])}]$/ && s !~ /[=;]/) return 1
    
    return 0
}

# Функция сокращения массива
function red(content,   e, n, i, out, r_cnt) {
    # Делим по запятым
    n = split(content, e, ",")
    if (n < 7) return content

    # Проверяем на "литеральность" края (3 с начала, 3 с конца)
    for (i = 1; i <= 3; i++) if (!is_lit(e[i])) return content
    for (i = n - 2; i <= n; i++) if (!is_lit(e[i])) return content

    # Формируем результат: первые 3, метка, последние 3
    # Очищаем элементы от лишних переносов для компактности
    for (i = 1; i <= n; i++) gsub(/^[ \t\n\r]+|[ \t\n\r]+$/, "", e[i])
    
    out = e[1] ", " e[2] ", " e[3] " , .... REDUCED .... , " e[n-2] ", " e[n-1] ", " e[n]
    return out
}

# 1. Накапливаем весь вход в одну переменную
{
    all_text = all_text $0 "\n"
}

# 2. Обрабатываем всё по завершении ввода
END {
    line = all_text
    
    # Ищем самые глубокие (вложенные) скобки: [ ], ( ), { }
    # Регулярка [\[({][^][(){}]*[\]})] находит скобки, внутри которых нет других скобок.
    while (match(line, /[\[({][^][(){}]*[\]})]/)) {
        start = RSTART
        len = RLENGTH
        
        pre = substr(line, 1, start - 1)
        full = substr(line, start, len)
        post = substr(line, start + len)
        
        # Разбираем найденный блок на скобки и тело
        op = substr(full, 1, 1)
        cl = substr(full, len, 1)
        body = substr(full, 2, len - 2)
        
        # Если внутри есть запятые — пытаемся редуцировать
        if (body ~ /,/) {
            body = red(body)
        }
        
        # Выводим префикс и обработанный блок, продолжаем с остатком
        printf "%s%s%s%s", pre, op, body, cl
        line = post
    }
    # Выводим хвост сообщения
    printf "%s", line
}
' "$@"
